//............ Seleccion de elementos -------------//
const botonencriptador1 = document.querySelector(".boton_encriptador");
const textoencriptador1 = document.querySelector(".encriptador1");
const aviso = document.querySelector(".texto_aviso");
const respuesta = document.querySelector(".evaluar");
const contenido = document.querySelector(".contenedor_tarjeta")
const botoncopiar = document.querySelector(".boton_copiar")
const botondesencriptar = document.querySelector(".boton_desencriptar")

//-----------Boton Encriptar-------------//
botonencriptador1.addEventListener("click", e=>{
    e.preventDefault();
    let texto = textoencriptador1.value;
    let txt = texto.normalize("NFD").replace(/[$\.¿\?~!\¡@#%^&*()_|}\{[\]>\<:"`;,\u0300-\u036f']/g, " " );
    
    if(texto == ""){
        aviso.style.background = "#0A3871";
        aviso.style.color = "#FFFF";
        aviso.style.fontWeight = "800";
        aviso.textContent = "El campo de texto no debe estar vacio";
        
        setTimeout(()=>{
            aviso.removeAttribute("style");
        },1500);
    }

    else if(texto !== txt){
        aviso.style.background = "#0A3871";
        aviso.style.color = "#FFFF";
        aviso.style.fontWeight = "800";
        aviso.textContent = "No debe tener asentos ni caracteres especiales";
        
        setTimeout(()=>{
            aviso.removeAttribute("style");
        },1500);
    }

    else if(texto !== texto.toLowerCase()){
        aviso.style.background = "#0A3871";
        aviso.style.color = "#FFFF";
        aviso.style.fontWeight = "800";
        aviso.textContent = "El texto debe ser todo en minuscula";
        
        setTimeout(()=>{
            aviso.removeAttribute("style");
        },1500);
    }

    else{
        texto = texto.replace(/e/mg, "enter");
        texto = texto.replace(/i/mg, "imes");
        texto = texto.replace(/a/mg, "ai");
        texto = texto.replace(/o/mg, "ober");
        texto = texto.replace(/u/mg, "ufat");

        respuesta.innerHTML = texto;
        botoncopiar.style.visibility = "inherit";
        contenido.remove();
    }

    
 });


